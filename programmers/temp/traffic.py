def solution(lines):
    answer = 0
    times = []
    for line in lines:
        start = line[11:23]
        hour = int(start[:2])*60*60*1000
        minute = int(start[3:5])*60*1000
        sec = int(float(start[6:])*1000)
        work_time = int(float(line[24:-1])*1000)
        time = hour + minute + sec
        times.append(((time-work_time), time))
    max_cnt = 0
    for s in times:
        for i in range(s[0], s[1]+1):
            start = i
            end = i + 999
            cnt = 0
            for time in times:
                if time[1] < start or time[0] > end:
                    continue
                for t in range(time[0], time[1]+1):
                    if start < t <= end:
                        cnt += 1
                        break
            if cnt > max_cnt:
                max_cnt = cnt
    return max_cnt


